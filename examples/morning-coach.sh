#!/data/data/com.termux/files/usr/bin/bash
# ============================================================
# morning-coach.sh — 早间规划提醒
# ============================================================
# cron 建议: 3 8 * * *  (每天早上 8:03)
#
# 通过 claude-relay 发送一条早间规划提醒。
# AI 会根据 prompt 生成个性化内容；API 失败则发送静态文案。
# ============================================================

~/claude-relay.sh \
  "你是用户的早间助手。现在是早上，用一条简洁的通知（100字内）：1) 问候早安 2) 提醒列出今天三件最重要的事 3) 一句话健康提醒。语气简洁直接。" \
  "早安" \
  "新的一天。写下今天三件一定要做的事，然后开干。记得每小时站起来活动一下。"
